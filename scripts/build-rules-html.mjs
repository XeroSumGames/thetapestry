#!/usr/bin/env node
// Wix export — Strategy B.
//
// Fetches every /rules/* page from the live Tapestry site, extracts the
// <main> body, inlines a wrapper for background/font, and rewrites every
// relative link/anchor target to an absolute URL on the Tapestry domain.
// Writes one HTML file per route to out/wix-rules/.
//
// Usage:
//   node scripts/build-rules-html.mjs
//   node scripts/build-rules-html.mjs --base http://localhost:3000
//
// Then on Wix: open each HTML file, copy its contents, paste into a Wix
// HTML-embed block on the matching page (Communities, Combat, etc.).

import { mkdir, writeFile } from 'node:fs/promises'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const REPO_ROOT = resolve(__dirname, '..')
const OUT_DIR = resolve(REPO_ROOT, 'out/wix-rules')
const PROD_BASE = 'https://thetapestry.distemperverse.com'

const args = process.argv.slice(2)
const baseIdx = args.indexOf('--base')
const FETCH_BASE = baseIdx >= 0 ? args[baseIdx + 1] : PROD_BASE
// Even when fetching from localhost, links inside the exported HTML always
// point at the live site — Wix visitors should land on Tapestry, not localhost.
const LINK_BASE = PROD_BASE

// Hard-coded slug list — matches lib/rules/sections.ts. Keeping a static
// list here (rather than dynamic-importing the .ts file) avoids needing
// ts-node or a build step before running the script.
const SLUGS = [
  '', // index page (/rules)
  'overview',
  'core-mechanics',
  'character-overview',
  'character-creation',
  'skills',
  'combat',
  'equipment',
  'communities',
  'appendix-tables',
  'appendix-skills',
  'appendix-equipment',
  'appendix-paradigms',
]

async function fetchPage(slug) {
  const url = slug ? `${FETCH_BASE}/rules/${slug}` : `${FETCH_BASE}/rules`
  const res = await fetch(url)
  if (!res.ok) throw new Error(`${url} → ${res.status}`)
  return await res.text()
}

// Pull <main …>…</main> out of the full HTML document. The /rules layout
// always wraps content in exactly one <main>, so a non-greedy match is safe.
function extractMain(html) {
  const m = html.match(/<main\b[^>]*>([\s\S]*?)<\/main>/i)
  if (!m) throw new Error('No <main> element found in page HTML')
  return m[1]
}

// Rewrite href="/foo" → href="https://thetapestry.distemperverse.com/foo".
// Leaves anchor-only links (#id), already-absolute http(s) links, and
// mailto: alone. This is the "external link out from Wix" rewrite.
function absolutizeLinks(html) {
  return html.replace(
    /\bhref="(\/[^"#][^"]*)"/g,
    (_, path) => `href="${LINK_BASE}${path}"`,
  )
}

// Strip Next.js dev/runtime boilerplate that sometimes leaks into <main>
// (script tags, hydration markers). Anything inside the <main> children
// that isn't part of our render is noise to Wix.
function clean(html) {
  return html
    .replace(/<script\b[\s\S]*?<\/script>/gi, '')
    .replace(/<noscript\b[\s\S]*?<\/noscript>/gi, '')
    .trim()
}

function wrap(slug, body) {
  const title =
    slug === ''
      ? 'XSE SRD'
      : `${slug.replace(/-/g, ' ').replace(/\b\w/g, c => c.toUpperCase())} — XSE SRD`
  return `<!-- Auto-generated by scripts/build-rules-html.mjs.
     Source: ${FETCH_BASE}/rules/${slug}
     Paste this entire block into a Wix HTML-embed widget. -->
<div data-tapestry-rules="${slug || 'index'}" style="background:#0f0f0f; color:#f5f2ee; font-family:Barlow,sans-serif; padding:2.5rem 2rem 5rem; max-width:880px; margin:0 auto;">
${body}
<hr style="margin-top:3rem; border:0; border-top:1px solid #2e2e2e;" />
<p style="font-size:13px; color:#7a7a7a; line-height:1.7; margin-top:1rem;">
  Live version: <a href="${LINK_BASE}/rules/${slug}" style="color:#7ab3d4;">${title}</a> · Source: <code>XSE SRD v1.1.17</code>
</p>
</div>
`
}

async function main() {
  await mkdir(OUT_DIR, { recursive: true })
  console.log(`Fetching from: ${FETCH_BASE}`)
  console.log(`Writing to: ${OUT_DIR}\n`)
  let okCount = 0
  let failCount = 0
  for (const slug of SLUGS) {
    const filename = (slug || 'index') + '.html'
    try {
      const full = await fetchPage(slug)
      const main = extractMain(full)
      const cleaned = clean(main)
      const linked = absolutizeLinks(cleaned)
      const wrapped = wrap(slug, linked)
      await writeFile(resolve(OUT_DIR, filename), wrapped, 'utf8')
      console.log(`✓ ${filename} (${wrapped.length} bytes)`)
      okCount++
    } catch (err) {
      console.error(`✗ ${filename} — ${err.message}`)
      failCount++
    }
  }
  console.log(`\n${okCount} ok, ${failCount} failed`)
  if (failCount > 0) process.exit(1)
}

main().catch(err => {
  console.error(err)
  process.exit(1)
})
