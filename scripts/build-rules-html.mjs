#!/usr/bin/env node
// Wix export — Strategy B.
//
// Fetches every /rules/* page from the live Tapestry site, extracts the
// <main> body, inlines a wrapper for background/font, and rewrites every
// relative link/anchor target to an absolute URL on the Tapestry domain.
// Writes one HTML file per route to out/wix-rules/.
//
// Usage:
//   node scripts/build-rules-html.mjs
//   node scripts/build-rules-html.mjs --base http://localhost:3000
//
// Then on Wix: open each HTML file, copy its contents, paste into a Wix
// HTML-embed block on the matching page (Communities, Combat, etc.).

import { mkdir, writeFile } from 'node:fs/promises'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const REPO_ROOT = resolve(__dirname, '..')
const OUT_DIR = resolve(REPO_ROOT, 'out/wix-rules')
const PROD_BASE = 'https://thetapestry.distemperverse.com'

const args = process.argv.slice(2)
const baseIdx = args.indexOf('--base')
const FETCH_BASE = baseIdx >= 0 ? args[baseIdx + 1] : PROD_BASE
// Even when fetching from localhost, links inside the exported HTML always
// point at the live site — Wix visitors should land on Tapestry, not localhost.
const LINK_BASE = PROD_BASE

// Hard-coded route map — matches lib/rules/sections.ts. After the
// 2026-05-01 SectionHub redesign each section is a hub + N anchor
// sub-pages; this script needs to fetch every leaf so Wix gets full
// content per page (the hub alone is just a card grid).
//
// Format: { hub: 'overview', subs: ['introduction', 'key-features', …] }
// hub === '' is the /rules landing page.
const SECTIONS = [
  { hub: '', subs: [] },
  { hub: 'overview', subs: ['introduction', 'key-features', 'naming-conventions'] },
  { hub: 'core-mechanics', subs: ['dice-check', 'modifiers', 'insight-dice', 'filling-in-the-gaps', 'making-the-case', 'attribute-checks', 'first-impressions'] },
  { hub: 'character-overview', subs: ['rapid', 'skills', 'secondary-stats', 'fleshing-out'] },
  { hub: 'character-creation', subs: ['backstory-generation', 'paradigms', 'character-evolution'] },
  { hub: 'skills', subs: ['skill-list', 'inspiration', 'psychology'] },
  { hub: 'combat', subs: ['combat-rounds', 'range', 'damage', 'incapacitation', 'stress'] },
  { hub: 'equipment', subs: ['item-condition', 'item-traits', 'upkeep'] },
  { hub: 'communities', subs: ['recruitment', 'apprentices', 'morale', 'structure', 'crb-additions'] },
  { hub: 'appendix-tables', subs: [] },
  { hub: 'appendix-skills', subs: [] },
  { hub: 'appendix-equipment', subs: [] },
  { hub: 'appendix-paradigms', subs: [] },
]

// Flatten into the list of routes the script will fetch + write.
// Each entry: { slug, urlPath, outFile }
const ROUTES = SECTIONS.flatMap(({ hub, subs }) => {
  const hubRoute = {
    slug: hub,
    urlPath: hub ? `/rules/${hub}` : '/rules',
    outFile: hub ? `${hub}/index.html` : 'index.html',
  }
  const subRoutes = subs.map(sub => ({
    slug: `${hub}/${sub}`,
    urlPath: `/rules/${hub}/${sub}`,
    outFile: `${hub}/${sub}.html`,
  }))
  return [hubRoute, ...subRoutes]
})

async function fetchPage(urlPath) {
  const url = `${FETCH_BASE}${urlPath}`
  const res = await fetch(url)
  if (!res.ok) throw new Error(`${url} → ${res.status}`)
  return await res.text()
}

// Pull <main …>…</main> out of the full HTML document. The /rules layout
// always wraps content in exactly one <main>, so a non-greedy match is safe.
function extractMain(html) {
  const m = html.match(/<main\b[^>]*>([\s\S]*?)<\/main>/i)
  if (!m) throw new Error('No <main> element found in page HTML')
  return m[1]
}

// Rewrite href="/foo" → href="https://thetapestry.distemperverse.com/foo".
// Leaves anchor-only links (#id), already-absolute http(s) links, and
// mailto: alone. This is the "external link out from Wix" rewrite.
function absolutizeLinks(html) {
  return html.replace(
    /\bhref="(\/[^"#][^"]*)"/g,
    (_, path) => `href="${LINK_BASE}${path}"`,
  )
}

// Strip Next.js dev/runtime boilerplate that sometimes leaks into <main>
// (script tags, hydration markers). Anything inside the <main> children
// that isn't part of our render is noise to Wix.
function clean(html) {
  return html
    .replace(/<script\b[\s\S]*?<\/script>/gi, '')
    .replace(/<noscript\b[\s\S]*?<\/noscript>/gi, '')
    .trim()
}

function wrap(urlPath, body) {
  const title =
    urlPath === '/rules'
      ? 'XSE SRD'
      : urlPath.replace('/rules/', '').replace(/-/g, ' ').replace(/\b\w/g, c => c.toUpperCase()) + ' — XSE SRD'
  return `<!-- Auto-generated by scripts/build-rules-html.mjs.
     Source: ${FETCH_BASE}${urlPath}
     Paste this entire block into a Wix HTML-embed widget. -->
<div data-tapestry-rules="${urlPath}" style="background:#0f0f0f; color:#f5f2ee; font-family:Barlow,sans-serif; padding:2.5rem 2rem 5rem; max-width:880px; margin:0 auto;">
${body}
<hr style="margin-top:3rem; border:0; border-top:1px solid #2e2e2e;" />
<p style="font-size:13px; color:#7a7a7a; line-height:1.7; margin-top:1rem;">
  Live version: <a href="${LINK_BASE}${urlPath}" style="color:#7ab3d4;">${title}</a> · Source: <code>XSE SRD v1.1.17</code>
</p>
</div>
`
}

async function main() {
  await mkdir(OUT_DIR, { recursive: true })
  console.log(`Fetching from: ${FETCH_BASE}`)
  console.log(`Writing to: ${OUT_DIR}`)
  console.log(`Routes to fetch: ${ROUTES.length}\n`)
  let okCount = 0
  let failCount = 0
  for (const route of ROUTES) {
    const outPath = resolve(OUT_DIR, route.outFile)
    try {
      const full = await fetchPage(route.urlPath)
      const main = extractMain(full)
      const cleaned = clean(main)
      const linked = absolutizeLinks(cleaned)
      const wrapped = wrap(route.urlPath, linked)
      await mkdir(dirname(outPath), { recursive: true })
      await writeFile(outPath, wrapped, 'utf8')
      console.log(`✓ ${route.outFile} (${wrapped.length} bytes)`)
      okCount++
    } catch (err) {
      console.error(`✗ ${route.outFile} — ${err.message}`)
      failCount++
    }
  }
  console.log(`\n${okCount} ok, ${failCount} failed`)
  if (failCount > 0) process.exit(1)
}

main().catch(err => {
  console.error(err)
  process.exit(1)
})
